namespace Hmm.Utility.MeasureUnit
{
    /// <summary>
    /// Enumeration for dimension unit
    /// </summary>
    public enum DimensionUnit
    {
        /// <summary>
        /// The millimeter unit, this is the
        /// default unit in the system
        /// </summary>
        Millimetre = 0,

        /// <summary>
        /// The centimeter unit
        /// </summary>
        Centimetre,

        /// <summary>
        /// The meter unit
        /// </summary>
        Metre,

        /// <summary>
        /// The kilometer unit
        /// </summary>
        Kilometre,

        /// <summary>
        /// The inch unit
        /// </summary>
        Inch,

        /// <summary>
        /// The feet unit
        /// </summary>
        Feet,

        /// <summary>
        /// The mile unit
        /// </summary>
        Mile
    }
}