namespace Hmm.Utility.MeasureUnit
{
    /// <summary>
    /// Enumeration for dimension unit
    /// </summary>
    public enum DimensionUnit
    {
        /// <summary>
        /// The inch unit
        /// </summary>
        Inch = 0,

        /// <summary>
        /// The feet unit
        /// </summary>
        Feet,

        /// <summary>
        /// The millimeter unit, this should be
        /// the default of the unit in the system
        /// </summary>
        Millimetre,

        /// <summary>
        /// The centimeter unit
        /// </summary>
        Centimetre,

        /// <summary>
        /// The meter unit
        /// </summary>
        Metre,

        /// <summary>
        /// The kilometer unit
        /// </summary>
        Kilometre
    }
}