namespace Hmm.Utility.MeasureUnit
{
    /// <summary>
    /// Enumeration for volume unit
    /// </summary>
    public enum VolumeUnit
    {
        /// <summary>
        /// The liter unit, this is the default unit in the system
        /// </summary>
        Liter = 0,

        /// <summary>
        /// The milliliter unit
        /// </summary>
        Milliliter,

        /// <summary>
        /// The centiliter unit
        /// </summary>
        Centiliter,

        /// <summary>
        /// The deciliter unit
        /// </summary>
        Deciliter,

        /// <summary>
        /// The cubic meter unit
        /// </summary>
        CubicMeter,

        /// <summary>
        /// The fluid ounce unit (US)
        /// </summary>
        Ounce,

        /// <summary>
        /// The pint unit (US)
        /// </summary>
        Pint,

        /// <summary>
        /// The quart unit (US)
        /// </summary>
        Quart,

        /// <summary>
        /// The gallon unit (US)
        /// </summary>
        Gallon,

        /// <summary>
        /// The bushel unit (US)
        /// </summary>
        Bushel
    }
}